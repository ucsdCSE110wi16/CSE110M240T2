public class Question {
    long number;
    String type;
    String answer;
    String image;
    String responses;

    public Question() {

    }

    public long getNumber() {
        return number;
    }

    public String getType() {
        return type;
    }

    public String getAnswer() {
        return answer;
    }

    public String getImage() {
        return image;
    }

    public String getResponses() {
        return responses;
    }
}
